package edu.skku.map.mapproject;


public class FirebaseMoney {
    public String expend;
    public String unit;
    public String date;
    public String purpose;
    public String url;
    public String people;



    public FirebaseMoney(String expend, String unit, String date, String purpose, String url, String people){
        this.expend=expend;
        this.unit=unit;
        this.date=date;
        this.purpose=purpose;
        this.url=url;
        this.people=people;
    }
}

