package edu.skku.map.mapproject;


public class FirebaseTrip {
    public String title;
    public String city;
    public String plan;
    public String plan2;
    public String budget;
    public String money;
    public String material;
    public String memo;
    public String url;
    public String type;



    public FirebaseTrip(String title, String city, String plan, String plan2, String budget, String money, String material, String memo, String url, String type){
        this.title=title;
        this.city=city;
        this.plan=plan;
        this.plan2=plan2;
        this.budget=budget;
        this.money=money;
        this.material=material;
        this.memo=memo;
        this.url=url;
        this.type=type;
    }
}

